package org.testapp.java.messenger.model;

public class Link {

	private String url;
	private String rel; //relationship attribute
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getRel() {
		return rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
	
	
}
